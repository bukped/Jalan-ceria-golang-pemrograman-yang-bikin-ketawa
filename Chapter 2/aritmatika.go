package main

import "fmt"

func main() {
	// Operator Aritmatika
	a := 10
	b := 5
	// Penjumlahan
	c := a + b
	fmt.Println("Penjumlahan:", c)
	// Pengurangan
	d := a - b
	fmt.Println("Pengurangan:", d)
	// Perkalian
	e := a * b
	fmt.Println("Perkalian:", e)
	// Pembagian
	f := a / b
	fmt.Println("Pembagian:", f)
	// Modulus (Sisa Pembagian)
	g := a % b
	fmt.Println("Modulus:", g)
}
